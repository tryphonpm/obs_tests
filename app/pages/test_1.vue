Cet ami a perdu les pédales.
	Un dernier coup de pompe au bucket
	et le voilà sorti du chemin des fleurs sauvages,
			en sa vallée.

La famille et ses porte-flingues
	tout en-costumés, tout en-parrainés
	rapacent déjà autour de lui
	et le gardent, rapprochés encore au plus près
		de la barricade toute neuve de son visage blanc.
L’exposé est quasi-méconnaissable,
	ce malgré les chuchotements attentionnés,
		à fleur de peau,
	de la taxidermie appliquée aux circonstances.
La circulation organisée s’épuise
et veut bâcler les choses :
	un dernier discours et on remballe.

Sur un bout de papier, un horizon de cinq lignes.
L’ami a pré-composé son chant noir
et a croché ce qu’il aurait souhaité entendre
	si son corps avait pu le dépanner une fois de plus.
Les cinq lignes notent les cadences à interpréter.
	On y reconnaît son engagement
	pour l’abolition des points d’orgues et du baroque.
Une partition assez courte pour nous éviter de finir à pas d’heure,
	dans cette nouvelle nuit ouverte.
L’absent a su couper à l’avance l’herbe sous les pieds de l’ennui,
	qui, à l’affût, impatient, précipite toujours la dernière danse.
	Merci à toi.

Je me rapproche de la maigreur du buffet à jus,
	rôdant autour de la médisance, péché premium.
	On ne chante pas encore mais ça va venir.
Pour le moment, on reprend la rigole
	là où le passé toujours présent l’avait laissée.
Et ça traîne un peu en longueur, c’est vrai mais on attend quoi ?

On finit bientôt par empiler bruyamment
	tables et chaises louées, tout plastic,
	dans un coin replié de ce lieu si laid, si polyvalent.

Les verres en toc, sacrifiés,
	craquent en hurlant sous les pas des fuyards.
Tout le monde visible a vraiment envie de rentrer au terrier.
	Ivres à demi, les survivants farfouillent dans leurs poches
	pour recoller au plus près de leurs clefs de bagnole.

Les chœurs : « On y va ? »
Les empreintes de tous ces pleutres
	marquent les poignées de portes
	et accumulent leur culpabilité
		avant le passage du karcher municipal
		qui, déjà, trépigne au coin
			dans l’impatience de sa corvée.

Ce jour-là,
	je n’ai versé aucune larme,
	je suis passé au travers.
Tiens ! Mon imagination
	m’a laissé tranquille,
	m’a laissé voir seulement ce qu’il y avait à voir de l’instant.
	L’après m’a totalement échappé.

« On y va  ? ...On y va.»
Dans la fuite, ne rien avouer
et surtout,
ne rien laisser au jugement de Dieu.

